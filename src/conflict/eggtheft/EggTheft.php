namespace conflict\eggtheft;

use pocketmine\plugin\PluginBase;

class EggTheft extends PluginBase {

	const MAP_CONFIG = "maps.json";
	const SETTINGS_CONFIG = "settings.yml";

	public function onEnable() {
		$this->loadConfigs();
	}

	public function loadConfigs() {
		if(!is_dir($this->getDataFolder())) {
			mkdir($this->getDataFolder(), 777);
			$this->saveResource(self::MAP_CONFIG);
			$this->saveResource(self::SETTINGS_CONFIG);
		}
	}

	public function onDisable() {
		parent::onDisable(); // TODO: Change the autogenerated stub
	}

}